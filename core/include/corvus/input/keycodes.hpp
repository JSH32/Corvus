enum Modifier : uint8_t {
    Mod_None  = 0,
    Mod_Shift = 1 << 0,
    Mod_Ctrl  = 1 << 1,
    Mod_Alt   = 1 << 2,
    Mod_Super = 1 << 3
};

inline bool hasModifier(uint8_t mods, Modifier mod) { return (mods & mod) != 0; }

